# Q1) Calculate the body mass index (BMI) of two variables input by the user, where:
#     BMI= weight/(height)^2.

weight = float(input("Enter your weight: "))
height = float(input("Enter your height"))
bmi = weight/(height ** 2)
print(bmi)
